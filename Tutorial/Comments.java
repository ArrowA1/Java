 public class Comments
 {
     public static void main(String[] args)
     {
         // these are single line comments
        System.out.println("Use \"//text goes here\" for single line comments");

         /* These are
            multiple line comments
        */
        System.out.println("Use \"/*text goes here*/\" for multiple line comments");

        /**
         * These are used 
         * to generate documentation
         */
        System.out.println("Use \"/**text goes here*/\" for documentation");
     }

 }